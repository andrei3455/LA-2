print("Comparison Operators :)")
x=int(input("X: "))
y=int(input("Y: "))
xgty=x>y
xgtety=x>=y
xlty=x<y
xltety=x<=y
xey=x==y
xney=x!=y
print(f"{x} > {y} = {xgty}")
print(f"{x} >= {y} = {xgtety}")
print(f"{x} < {y} = {xlty}")
print(f"{x} <= {y} = {xltety}")
print(f"{x} == {y} = {xey}")
print(f"{x} != {y} = {xney}")
