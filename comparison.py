print("Comparison Operators")
x=int(input("X: "))
y=int(input("Y: "))
xgty=x>y
xgtety=x>=y
xlty=x<y
xltety=x<=y
xey=x==y
xney=x!=y
print(str(x)+">"+str(y)+":"+str(xgty))
print(str(x)+">="+str(y)+":"+str(xgtety))
print(str(x)+"<"+str(y)+":"+str(xlty))
print(str(x)+"<="+str(y)+":"+str(xltety))
print(str(x)+"=="+str(y)+":"+str(xey))
print(str(x)+"!="+str(y)+":"+str(xney))
